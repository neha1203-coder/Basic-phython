list = [53,69,34,75,85]
print(list)
list.append(43)           # append means adding elements in the list that's why list is mutable
print(list)
list.sort()               # arrange the list in asecending order
print(list)
list.sort(reverse=True)    # arrange the list in decending order
print(list)
list.reverse()           # reverse the list
print(list)
list.insert(2,49)        # insert element at index  list.insert(index,element)
print(list)
